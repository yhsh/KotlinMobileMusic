object CursorUtil {
    fun logCursor(cursor: Cursor?): String {
        val sb = StringBuffer()
        cursor?.let {
            it.moveToPosition(-1)
            while (it.moveToNext()) {
                for (index in 0 until it.columnCount) {
                    sb.append("key = ${it.getColumnName(index)},value = ${it.getString(index)}").append(":")
//                    println("key = ${it.getColumnName(index)},value = ${it.getString(index)}")
                }
            }
        }
        return sb.toString()
    }
}